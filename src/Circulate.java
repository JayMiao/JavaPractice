public class Circulate {
    public static void main(String args[]) {
        int[] numbers = {10,20,30,40,50};

        for(int x:numbers) {
            System.out.println(x);
        }

        String[] names = {"a","b","c"};
        for (String name:names) {
            System.out.println(name);
        }
    }
}
